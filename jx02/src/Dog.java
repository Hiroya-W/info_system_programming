public class Dog {
    // インスタンス変数
    private String name;
    private String voice;

    // メソッド
    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public String getVoice() {
        return voice;
    }

    public void setVoice(String voice) {
        this.voice = voice;
    }
}
